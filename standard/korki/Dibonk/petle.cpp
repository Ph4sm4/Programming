#include <iostream>
using namespace std;

int main()
{

    /*
        i++ oznacza zwiekszenie liczby o 1 -> i = i + 1 lub i += 1
        i++ zawsze zwiekszy ci o 1 i tylko o 1

        stworz zmienne: Hello="Hello" Oraz World="World";
        nastepnie w petli 100 razy:
            wypisz na ekran wynik operacji Hello + " " + World;
    */

    for (int i = 0;; i++)
    {
        cout << i << " ";
    }

    return 0;
}